/// Normalizes a locale code for ARB filename by removing script identifiers.
/// 
/// Flutter ARB files should not contain script names in the filename.
/// Script identifiers are typically 4-letter codes (e.g., "Hans", "Hant", "Cyrl", "Mong").
/// 
/// Examples:
/// - "zh-Hans" -> "zh"
/// - "mn-Mong" -> "mn"
/// - "zh-Hans-CN" -> "zh-CN"
/// - "en" -> "en"
/// - "en-US" -> "en-US"
String normalizeLocaleForArbFilename(String locale) {
  final parts = locale.split('-');
  if (parts.length == 1) {
    // Simple locale code like "en", "fr"
    return locale;
  }
  
  // Filter out script identifiers (4-letter codes)
  // Keep language code (first part) and region codes (2-3 letter codes)
  final normalizedParts = <String>[parts[0]]; // Always keep language code
  
  for (int i = 1; i < parts.length; i++) {
    final part = parts[i];
    // Script identifiers are 4 letters, regions are 2-3 letters
    // Keep only non-script parts (regions)
    if (part.length != 4) {
      normalizedParts.add(part);
    }
  }
  
  return normalizedParts.join('-');
}
