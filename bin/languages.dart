class Language {
  const Language(this.locale, this.nameEn, this.name);

  final Locale locale;
  final String nameEn;
  final String name;

  Language.fromMap(Map<String, String> map)
      : nameEn = map['nameEn']!,
        locale = Locale(map['locale']!),
        name = map['name']!;

  static const empty = Language(Locale.fromSubtags(), '', '');

  /// Returns the Language matching the given ISO code from the standard list.
  factory Language.fromIsoCode(String code) => Languages.defaultLanguages
      .firstWhere((l) => l.locale.languageCode == code);
//locale, nameEng, name
  @override
  String toString() => "('${locale.toString()}', '$nameEn', '$name'),";

  @override
  bool operator ==(other) =>
      other is Language && nameEn == other.nameEn && locale == other.locale;

  @override
  int get hashCode => locale.hashCode;
}

